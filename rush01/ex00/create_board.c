#include "globals.h"

void	create_board(void)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			g_board[i][j] = 0;
			j++;
		}
		i++;
	}
}
